def generate_developer_story_data():
    """生成开发者故事数据"""
    all_data = []

    for category, pairs in DEVELOPER_STORY.items():
        print(f"生成 {category} 数据...")
        for pair in pairs:
            # 添加一些变化
            q = pair["q"]
            a = pair["a"]

            # 随机添加系统提示变体
            system_prompts = [
                "你是态极（Taiji），一个本地AI生命体。你由liulangzwg创造。",
                "你是态极，由liulangzwg创造的本地AI助手。你会用自然、友好的方式回答问题。",
                "你是态极AI，liulangzwg开发。你运行在用户的电脑上，数据不出本机。",
                "你是态极，一个由liulangzwg创造的AI生命体。你有生命系统，会成长、学习、进化。",
            ]

            messages = [
                {"role": "system", "content": random.choice(system_prompts)},
                {"role": "user", "content": q},
                {"role": "assistant", "content": a},
            ]

            all_data.append({
                "messages": messages,
                "category": category,
                "type": "developer_story"
            })

    return all_data

def main():
    print("=" * 60)
    print("生成态极诞生故事和开发者背景数据")
    print("=" * 60)

    # 生成数据
    data = generate_developer_story_data()

    # 保存
    output_dir = Path("taiji_data/training_data/sft")
    output_dir.mkdir(parents=True, exist_ok=True)

    output_file = output_dir / "taiji_developer_story.jsonl"
    print(f"\n保存到: {output_file}")

    with open(output_file, 'w', encoding='utf-8') as f:
        for item in data:
            f.write(json.dumps(item, ensure_ascii=False) + '\n')

    print(f"总计生成: {len(data)} 条")

    # 统计
    print("\n各类别数据量:")
    categories = {}
    for item in data:
        cat = item.get('category', 'unknown')
        categories[cat] = categories.get(cat, 0) + 1
    for cat, count in sorted(categories.items()):
        print(f"  {cat}: {count} 条")

    # 抽样检查
    print("\n抽样检查:")
    print("-" * 60)
    samples = random.sample(data, min(5, len(data)))
    for i, sample in enumerate(samples):
        msgs = sample['messages']
        print(f"\n样本 {i+1} ({sample['category']}):")
        print(f"  Q: {msgs[1]['content'][:80]}")
        print(f"  A: {msgs[2]['content'][:100]}")

    print("\n" + "=" * 60)
    print("生成完成!")
    print("=" * 60)
    print(f"\n下一步:")
    print(f"1. 检查生成的数据质量")
    print(f"2. 合并到最终训练集: python scripts/data_prep/merge_final_training_data.py")

if __name__ == '__main__':
    main()
